def kl_div(p_logit, q_logit, T):
    p = F.softmax(p_logit / T, dim=-1)
    kl = torch.sum(p * (F.log_softmax(p_logit / T, dim=-1)
                                  - F.log_softmax(q_logit / T, dim=-1)), 1)
    return torch.mean(kl)


def kl_div_mask(p_logit, q_logit, T, mask, s_ctrl=0.5):
    p = F.softmax(p_logit / T, dim=-1)
    kl = ( 1 - (1-mask) + s_ctrl*(1-mask) ) * ( torch.sum(p * (F.log_softmax(p_logit / T, dim=-1)
                                  - F.log_softmax(q_logit / T, dim=-1)), 1) )
    return torch.mean(kl)


def l1_loss_segmentation(logits, labels, num_classes, ignore_index=255):
    """
    计算语义分割的L1损失
    :param logits: 模型输出 [B, C, H, W]
    :param labels: 标签 [B, H, W]
    :param num_classes: 类别数量
    :param ignore_index: 忽略标签值 (默认255)
    :return: L1损失值
    """
    # 1. 转换标签为one-hot
    one_hot = torch.nn.functional.one_hot(
        torch.clamp(labels, 0, num_classes-1),  # 防止越界
        num_classes=num_classes
    ).permute(0, 3, 1, 2).float()
    
    # 2. 创建忽略区域掩码
    valid_mask = (labels != ignore_index).unsqueeze(1)  # [B, 1, H, W]
    
    # 3. 计算绝对误差并应用掩码
    logits = F.softmax(logits, dim=-1)
    abs_diff = torch.abs(logits - one_hot)
    masked_diff = abs_diff * valid_mask
    
    # 4. 计算有效像素数量 (防止除零)
    num_valid = valid_mask.sum().item()
    
    # 5. 返回平均L1损失
    return masked_diff.sum() / (num_valid * num_classes + 1e-8)


def dist_s_t(p_logit, q_logit, T):
    p = F.softmax(p_logit / T, dim=-1)
    q = F.softmax(q_logit / T, dim=-1)
    dist = torch.sum(torch.abs(q - p), 1)

    return torch.mean(dist)


def compute_consistency_mask(outputs_t, labels, ignore_index=255):
    """
    计算模型预测与原始标签一致的像素掩码，处理忽略区域
    :param outputs_t: 模型输出概率图 [B, C, H, W]
    :param labels: 原始标签 [B, H, W]
    :param ignore_index: 忽略标签值（默认255）
    :return: 一致性掩码 [B, H, W] (True表示预测正确且非忽略区域)
    """
    # 1. 获取预测类别索引
    pred_indices = outputs_t.detach().max(dim=1)[1]
    
    # 2. 创建有效区域掩码（非忽略区域）
    valid_mask = (labels != ignore_index)
    
    # 3. 比较预测和标签
    correct_mask = (pred_indices == labels)
    
    # 4. 只考虑非忽略区域的正确预测
    consistency_mask = correct_mask & valid_mask
    
    return consistency_mask


